package com.mateusborja.samples.expressoes;

public class Exemplo02 {

    public static void main(String[] args) {

        int x, y, operacao;

        x = 15;
        y = 4;
        operacao = x % y; //15 dividido por 4 igual a 3 = 15 menos 12  = 3 (logo resto e 3)
        System.out.println(operacao);

        x = 25;
        y = 6;
        operacao = x % y;
        System.out.println(operacao);

        x = 18;
        y = 4;
        operacao = x % y;
        System.out.println(operacao);


    }
}
